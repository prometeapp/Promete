namespace Promete.Nodes;

/// <summary>
/// 図形の種類を表す列挙型です。
/// </summary>
public enum ShapeType
{
    /// <summary>
    /// ピクセル（点）
    /// </summary>
    Pixel,

    /// <summary>
    /// 線
    /// </summary>
    Line,

    /// <summary>
    /// 四角形
    /// </summary>
    Rect,

    /// <summary>
    /// 三角形
    /// </summary>
    Triangle,

    /// <summary>
    /// 多角形
    /// </summary>
    Polygon
}
