namespace Promete.Graphics.Fonts;

/// <summary>
/// フォントのスタイルを定義する列挙型です。
/// </summary>
public enum FontStyle
{
    /// <summary>
    /// 通常のフォントスタイルです。
    /// </summary>
    Normal,

    /// <summary>
    /// 太字のフォントスタイルです。
    /// </summary>
    Bold,

    /// <summary>
    /// 斜体のフォントスタイルです。
    /// </summary>
    Italic,

    /// <summary>
    /// 太字かつ斜体のフォントスタイルです。
    /// </summary>
    BoldItalic
}
